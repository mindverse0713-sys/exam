// Function to reorder questions for variant B
// B[i] = A[(i + 9) % 20 + 1] for 1-indexed
function createVariantB(variantA) {
  const questionsB = [];
  const answersB = {};
  
  for (let i = 1; i <= 20; i++) {
    // Map: B[1] = A[11], B[2] = A[12], ..., B[10] = A[20], B[11] = A[1], ..., B[20] = A[10]
    const sourceIndex = i <= 10 ? i + 10 : i - 10;
    questionsB.push(variantA.questions[sourceIndex - 1]);
    answersB[String(i)] = variantA.answers[String(sourceIndex)];
  }
  
  return {
    questions: questionsB,
    answers: answersB
  };
}

// Generate variant B for all grades
exams.grade10.variantB = createVariantB(exams.grade10.variantA);
exams.grade11.variantB = createVariantB(exams.grade11.variantA);
exams.grade12.variantB = createVariantB(exams.grade12.variantA);

// Convert to JSON format for database
function formatForDatabase(grade, variant, data) {
  return {
    grade: parseInt(grade),
    variant: variant,
    sections_public: {
      mcq: data.questions
    },
    answer_key: {
      mcqKey: data.answers
    },
    active: true
  };
}

